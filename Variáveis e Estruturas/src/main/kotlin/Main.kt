fun main(args: Array<String>) {
    /*
    * Estrutura de uma variável:
    * mutabilidade nome: tipo
    * Mutabilidade -> var ou val
    * nome -> camelCase seguido de ":"
    * Tipo: Int, Double, Float, Long, Char, String, Short, byte,
    * U(Somente positivos, seguidos de um tipo numerico)
    * (Iniciando com letra maiúscula)
    */
    var x: Int // (var) Variável mutável
    val y: Int // (val) Variável IMUtável
